package variables;

public class EjemploTiposDatos {

	public static void main(String[] args) {
		
		// números enteros
		byte b = 8;
		short peque = 123;
		int entero = 123456789;
		long grande = 12341324123411324L;
		long otroGrande = 123_456_789_123_456l;
		
		// números decimales
		float decimalPeque = 123.456f;
		double decimalGrande = 123089417234.12394123;
		double notacionCientifica = 1.1234132E-10;
		

		// booleanas
		boolean condicion = true;
		
		// Caracteres y cadenas
		char c = 'c'; // Comillas simples
		String mensaje = "Hola Mundo"; // Comillas dobles
		
		

	}

}
